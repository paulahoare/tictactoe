// Square does not need to be a Component (class), it only needs to be
// a "function component" because it only needs a render() method and does
// not have it's own state. A function component takes props as input and
// returns what should be rendered
function Square(props) {
  return (
    <button className="square" onClick={props.onClick}>
      {props.value}
    </button>
  );
}

